package Numbers;

public class SwitchCases {

	public static void main(String[] args) {
		int salary = 1;
		switch (salary) {
		case 1:
			System.out.println("weekly salary");
			break;

		case 2:
			System.out.println("Monthly salary");
			break;
		case 3:
			System.out.println("Yearly salary");
			break;
		default:
			System.out.println("Add all salary ");
			break;
		}

	}

}
